    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String palabra = "";
        String palabra2 = "";
        if (accion == 1) {
            palabra = "registrar";
            palabra2 = "registrado";

            if (JOptionPane.showConfirmDialog(null, "¿Desea " + palabra + " el Periodo?", "Mensaje del Sistema", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                pregimenPensionControlador.getSeleccionado().setFechaInicio(jdateInicial.getDate());
                pregimenPensionControlador.getSeleccionado().setFechaFin(jdateFinal.getDate());
                pregimenPensionControlador.getSeleccionado().setVigente(chckVigente.isSelected());
                pregimenPensionControlador.getSeleccionado().setRegimenPensionario((RegimenPensionario) cmbRegimenPension.getSelectedItem());
                pregimenPensionControlador.getSeleccionado().setNombre(((RegimenPensionario) cmbRegimenPension.getSelectedItem()).getDescripcion());

                pregimenPensionControlador.accion(accion);
                lista.add(pregimenPensionControlador.getSeleccionado());

                if (accion == 1) {
                    JOptionPane.showMessageDialog(null, "Periodo " + palabra2 + " correctamente", "Mensaje del Sistema", JOptionPane.INFORMATION_MESSAGE);
                    FormularioUtil.limpiarComponente(panelDatos);
                    cargarComboRegimenPension();
                } else {
                    JOptionPane.showMessageDialog(null, "Periodo no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                FormularioUtil.limpiarComponente(panelDatos);
                cargarComboRegimenPension();
                JOptionPane.showMessageDialog(null, "Periodo no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            }
        } else if (accion == 2) {
            palabra = "modificar";
            palabra2 = "modificado";

            if (JOptionPane.showConfirmDialog(null, "¿Desea " + palabra + " el Periodo?", "Mensaje del Sistema", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                if (accion == 2) {
                    JOptionPane.showMessageDialog(null, "Periodo " + palabra2 + " correctamente", "Mensaje del Sistema", JOptionPane.INFORMATION_MESSAGE);

                    lista.clear();

                    pregimenPensionControlador.getSeleccionado().setFechaInicio(jdateInicial.getDate());
                    pregimenPensionControlador.getSeleccionado().setFechaFin(jdateFinal.getDate());
                    pregimenPensionControlador.getSeleccionado().setVigente(chckVigente.isSelected());
                    pregimenPensionControlador.getSeleccionado().setRegimenPensionario((RegimenPensionario) cmbRegimenPension.getSelectedItem());
                    pregimenPensionControlador.getSeleccionado().setNombre(((RegimenPensionario) cmbRegimenPension.getSelectedItem()).getDescripcion());

                    pregimenPensionControlador.accion(accion);
                    listar();

                    FormularioUtil.limpiarComponente(panelDatos);
                    cargarComboRegimenPension();

                } else {
                    JOptionPane.showMessageDialog(null, "Periodo no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                FormularioUtil.limpiarComponente(panelDatos);
                cargarComboRegimenPension();
                JOptionPane.showMessageDialog(null, "Periodo no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            }
        }

        FormularioUtil.activarComponente(panelOpciones, true);
        FormularioUtil.activarComponente(panelGuardar, false);
        FormularioUtil.activarComponente(panelDatos, false);
    }//GEN-LAST:event_jButton4ActionPerformed

