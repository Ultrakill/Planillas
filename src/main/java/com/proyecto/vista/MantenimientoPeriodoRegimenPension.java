    private void listar() {
        lista = this.pregimenPensionControlador.buscarTodos();
        lista = ObservableCollections.observableList(lista);

        JTableBinding binding = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ, lista, tblPeriodoPension);

        BeanProperty bNombre = BeanProperty.create("nombre");
        BeanProperty bFechaInicio = BeanProperty.create("fechaInicio");
        BeanProperty bFechaFin = BeanProperty.create("fechaFin");
        BeanProperty bVigente = BeanProperty.create("vigente");
        BeanProperty bRegimenPension = BeanProperty.create("regimenPensionario");

        binding.addColumnBinding(bNombre).setColumnName("NOMBRE").setEditable(false);
        binding.addColumnBinding(bFechaInicio).setColumnName("FECHA_INICIO").setEditable(false);
        binding.addColumnBinding(bFechaFin).setColumnName("FECHA_FIN").setEditable(false);
        binding.addColumnBinding(bVigente).setColumnName("VIGENTE").setEditable(false);
        binding.addColumnBinding(bRegimenPension).setColumnName("REGIMEN_PENSIONARIO").setEditable(false);

        binding.bind();
    }

    private void cargarComboRegimenPension() {
        RegimenPensionarioControlador ac = new RegimenPensionarioControlador();
        List<RegimenPensionario> regimenPension = ac.buscarTodos();
        cmbRegimenPension.setModel(new DefaultComboBoxModel(regimenPension.toArray()));
        AutoCompleteDecorator.decorate(this.cmbRegimenPension);
    }
