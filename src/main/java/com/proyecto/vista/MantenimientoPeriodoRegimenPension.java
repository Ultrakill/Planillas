    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
        accion = AbstractControlador.MODIFICAR;
        cargarComboRegimenPension();
        int fila = this.tblPeriodoPension.getSelectedRow();
        if (fila != -1) {
            FormularioUtil.activarComponente(panelDatos, true);
            FormularioUtil.activarComponente(panelOpciones, false);
            FormularioUtil.activarComponente(panelGuardar, true);

            accion = AbstractControlador.MODIFICAR;
            pregimenPensionControlador.setSeleccionado(lista.get(fila));
            PeriodoRegimenPension pPension = pregimenPensionControlador.getSeleccionado();
 
            jdateInicial.setDate(pPension.getFechaInicio());
            jdateFinal.setDate(pPension.getFechaFin());
            chckVigente.setSelected(pPension.isVigente());
            
            cmbRegimenPension.setSelectedItem(pPension.getRegimenPensionario());

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Periodo", "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton17ActionPerformed

