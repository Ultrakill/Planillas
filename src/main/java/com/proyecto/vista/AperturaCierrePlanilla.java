    private void listar() {
        lista = this.planillaControlador.buscarTodos();
        lista = ObservableCollections.observableList(lista);

        JTableBinding binding = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ, lista, tblPlanillas);

        BeanProperty bId = BeanProperty.create("tipoPlanilla");
        BeanProperty bFechaInicio = BeanProperty.create("fechaInicial");
        BeanProperty bFechaFin = BeanProperty.create("fechaFinal");
        BeanProperty bAsigFam = BeanProperty.create("montoAsigFamiliar");
        BeanProperty bMontoVida = BeanProperty.create("montoEsSaludVida");
        BeanProperty bMontoEsSalud = BeanProperty.create("montoEsSalud");
        BeanProperty bMontoONP = BeanProperty.create("montoOnp");
        BeanProperty bEstado = BeanProperty.create("estado");

        binding.addColumnBinding(bId).setColumnName("TIPO").setEditable(false);
        binding.addColumnBinding(bFechaInicio).setColumnName("FECHA INICIO").setEditable(false);
        binding.addColumnBinding(bFechaFin).setColumnName("FECHA FIN").setEditable(false);
        binding.addColumnBinding(bAsigFam).setColumnName("ASIG. FAM.").setEditable(false);
        binding.addColumnBinding(bMontoVida).setColumnName("VIDA").setEditable(false);
        binding.addColumnBinding(bMontoEsSalud).setColumnName("EsSALUD").setEditable(false);
        binding.addColumnBinding(bMontoONP).setColumnName("ONP").setEditable(false);
        binding.addColumnBinding(bEstado).setColumnName("ESTADO").setEditable(false);

        binding.bind();
    }

    private void cargarComboEstado() {
        List<String> estado = new ArrayList<>();
        estado.add("E");
        estado.add("A");
        estado.add("O");
        cmbEstado.setModel(new DefaultComboBoxModel(estado.toArray()));
        AutoCompleteDecorator.decorate(this.cmbEstado);
    }

    private void cargarComboTipoPlanilla() {
        List<String> planilla = new ArrayList<>();
        planilla.add("ORDINARIO");
        planilla.add("CTS-SEMESTRAL");
        planilla.add("GRATIFICACION");
        cmbTipoPlanilla.setModel(new DefaultComboBoxModel(planilla.toArray()));
        AutoCompleteDecorator.decorate(this.cmbTipoPlanilla);
    }

    private void cargarCombos() {
        cargarComboEstado();
        cargarComboTipoPlanilla();
    }
