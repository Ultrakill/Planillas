    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        accion = AbstractControlador.MODIFICAR;
        int fila = this.tblPlanillas.getSelectedRow();
        if (fila != -1) {
            FormularioUtil.activarComponente(panelDatos, true);
            FormularioUtil.activarComponente(panelOpciones, false);
            FormularioUtil.activarComponente(panelGuardar, true);

            accion = AbstractControlador.MODIFICAR;
            planillaControlador.setSeleccionado(lista.get(fila));
            Planilla planilla = planillaControlador.getSeleccionado();

            jdateInicial.setDate(planilla.getFechaInicial());
            jdateFinal.setDate(planilla.getFechaFinal());
            jdatePago.setDate(planilla.getFechaPago());

            spnAsigFam.setValue(planilla.getMontoAsigFamiliar());
            spnVida.setValue(planilla.getMontoEsSaludVida());
            spnEsSalud.setValue(planilla.getMontoEsSalud());
            spnOnp.setValue(planilla.getMontoOnp());

            cmbEstado.setSelectedItem(planilla.getEstado());
            cmbTipoPlanilla.setSelectedItem(planilla.getTipoPlanilla());

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Contrato", "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String palabra = "";
        String palabra2 = "";
        if (accion == 1) {
            palabra = "registrar";
            palabra2 = "registrado";

            if (JOptionPane.showConfirmDialog(null, "¿Desea " + palabra + " el Ambiente?", "Mensaje del Sistema", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                planillaControlador.getSeleccionado().setFechaInicial(jdateInicial.getDate());
                planillaControlador.getSeleccionado().setFechaFinal(jdateFinal.getDate());
                planillaControlador.getSeleccionado().setFechaPago(jdatePago.getDate());
                planillaControlador.getSeleccionado().setMontoAsigFamiliar((Double) spnAsigFam.getValue());
                planillaControlador.getSeleccionado().setMontoEsSaludVida((Double) spnVida.getValue());
                planillaControlador.getSeleccionado().setMontoEsSalud((Double) spnEsSalud.getValue());
                planillaControlador.getSeleccionado().setMontoOnp((Double) spnOnp.getValue());

                planillaControlador.getSeleccionado().setTipoPlanilla(cmbTipoPlanilla.getSelectedItem().toString());
                planillaControlador.getSeleccionado().setEstado(cmbEstado.getSelectedItem().toString());

                planillaControlador.accion(accion);
                lista.add(planillaControlador.getSeleccionado());

                if (accion == 1) {
                    JOptionPane.showMessageDialog(null, "Contrato " + palabra2 + " correctamente", "Mensaje del Sistema", JOptionPane.INFORMATION_MESSAGE);
                    FormularioUtil.limpiarComponente(panelDatos);
                    cargarCombos();
                } else {
                    JOptionPane.showMessageDialog(null, "Contrato no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                FormularioUtil.limpiarComponente(panelDatos);
                cargarCombos();
                JOptionPane.showMessageDialog(null, "Contrato no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            }
        } else if (accion == 2) {
            palabra = "modificar";
            palabra2 = "modificado";

            if (JOptionPane.showConfirmDialog(null, "¿Desea " + palabra + " el Contrato?", "Mensaje del Sistema", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                if (accion == 2) {
                    JOptionPane.showMessageDialog(null, "Contrato " + palabra2 + " correctamente", "Mensaje del Sistema", JOptionPane.INFORMATION_MESSAGE);

                    lista.clear();
                    planillaControlador.getSeleccionado().setFechaInicial(jdateInicial.getDate());
                    planillaControlador.getSeleccionado().setFechaFinal(jdateFinal.getDate());
                    planillaControlador.getSeleccionado().setFechaPago(jdatePago.getDate());
                    planillaControlador.getSeleccionado().setMontoAsigFamiliar((Double) spnAsigFam.getValue());
                    planillaControlador.getSeleccionado().setMontoEsSaludVida((Double) spnVida.getValue());
                    planillaControlador.getSeleccionado().setMontoEsSalud((Double) spnEsSalud.getValue());
                    planillaControlador.getSeleccionado().setMontoOnp((Double) spnOnp.getValue());

                    planillaControlador.getSeleccionado().setTipoPlanilla(cmbTipoPlanilla.getSelectedItem().toString());
                    planillaControlador.getSeleccionado().setEstado(cmbEstado.getSelectedItem().toString());
                    planillaControlador.accion(accion);
                    listar();

                    FormularioUtil.limpiarComponente(panelDatos);
                    cargarCombos();

                } else {
                    JOptionPane.showMessageDialog(null, "Contrato no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                FormularioUtil.limpiarComponente(panelDatos);
                cargarCombos();
                JOptionPane.showMessageDialog(null, "Contrato no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            }
        }

        FormularioUtil.activarComponente(panelOpciones, true);
        FormularioUtil.activarComponente(panelGuardar, false);
        FormularioUtil.activarComponente(panelDatos, false);
    }//GEN-LAST:event_jButton1ActionPerformed

