    private void listar() {
        lista = this.contratoControlador.buscarTodos();
        lista = ObservableCollections.observableList(lista);

        JTableBinding binding = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ, lista, tblContratos);

        BeanProperty bId = BeanProperty.create("id");
        BeanProperty bFechaInicio = BeanProperty.create("fechaInicio");
        BeanProperty bFechaFin = BeanProperty.create("fechaFin");
        BeanProperty bSueldoBase = BeanProperty.create("sueldoBase");
        BeanProperty bPeriodicidad = BeanProperty.create("periodicidad");

        binding.addColumnBinding(bId).setColumnName("ID").setEditable(false);
        binding.addColumnBinding(bFechaInicio).setColumnName("FECHA_INICIO").setEditable(false);
        binding.addColumnBinding(bFechaFin).setColumnName("FECHA_FIN").setEditable(false);
        binding.addColumnBinding(bSueldoBase).setColumnName("SUELDO_BASE").setEditable(false);
        binding.addColumnBinding(bPeriodicidad).setColumnName("PERIODICIDAD").setEditable(false);

        binding.bind();
    }

