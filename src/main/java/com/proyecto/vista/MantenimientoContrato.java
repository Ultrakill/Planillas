    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        accion = AbstractControlador.MODIFICAR;
        int fila = this.tblContratos.getSelectedRow();
        if (fila != -1) {
            FormularioUtil.activarComponente(panelDatos, true);
            FormularioUtil.activarComponente(panelOpciones, false);
            FormularioUtil.activarComponente(panelGuardar, true);

            accion = AbstractControlador.MODIFICAR;
            contratoControlador.setSeleccionado(lista.get(fila));
            Contrato contrato = contratoControlador.getSeleccionado();

            jdateInicial.setDate(contrato.getFechaInicio());
            jdateFinal.setDate(contrato.getFechaFin());
            spnSueldo.setValue(contrato.getSueldoBase());
            chckVida.setSelected(contrato.isVida());
            chckAsigFam.setSelected(contrato.isAsignacionFam());
            chckVigenteC.setSelected(contrato.getVigente());

            cmbTipoPago.setSelectedItem(contrato.getTipoPago());
            cmbTipoContrato.setSelectedItem(contrato.getTipoContrato());
            cmbMotivoBaja.setSelectedItem(contrato.getMotivoBajaTrabajador());
//            cmbTipoTrabajador.setSelectedItem(contrato.getPeriodoTipoTrabajador().getTipoTrabajador().getDescripcion());
//            cmbRegimenPension.setSelectedItem(contrato.getPeriodoRegimenPension().getRegimenPensionario());
//            cmbRegimenSalud.setSelectedItem(contrato.getPeriodoRegimenSalud().getRegimenAseguramiento());
            cmbPeriodicidad.setSelectedItem(contrato.getPeriodicidad());

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Contrato", "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton13ActionPerformed

