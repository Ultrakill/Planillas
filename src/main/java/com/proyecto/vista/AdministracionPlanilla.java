    private void listarC() {
        contrato = this.contratoControlador.buscarTodos();
        contrato = ObservableCollections.observableList(contrato);

        BeanProperty bNombre = BeanProperty.create("empleado");
        BeanProperty bVigente = BeanProperty.create("vigente");

        JTableBinding binding = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ, contrato, tblContrato);

        binding.addColumnBinding(bNombre).setColumnName("NOMBRE").setEditable(false);
        binding.addColumnBinding(bVigente).setColumnName("CONTRATO").setEditable(false).setColumnClass(Boolean.class);

        binding.bind();
    }

    private void listarB() {
        contratoFiltrado = ObservableCollections.observableList(contratoFiltrado);

        BeanProperty bNombre = BeanProperty.create("empleado");
        BeanProperty bVigente = BeanProperty.create("vigente");

        JTableBinding binding = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ_ONCE, contratoFiltrado, tblDetalle);

        binding.addColumnBinding(bNombre).setColumnName("NOMBRE").setEditable(false);
        binding.addColumnBinding(bVigente).setColumnName("CONTRATO").setEditable(false).setColumnClass(Boolean.class);

        binding.bind();
    }

