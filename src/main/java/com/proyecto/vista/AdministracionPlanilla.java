    private Boolean prueba(Contrato testeo) {
        PeriodoRegimenPensionControlador prpControlador = new PeriodoRegimenPensionControlador();
        List<PeriodoRegimenPension> plist = prpControlador.buscarXContrato(testeo);
        Boolean bandera = false;
        for (PeriodoRegimenPension plist1 : plist) {
            if (plist1.isVigente()) {
                bandera = true;
            }
        }
        System.out.println("P_Regimen T: " + plist.size());

        List<PeriodoRegimenSalud> pslist = testeo.getPeriodoRegimenSaludList();
        Boolean bandera2 = false;
        for (PeriodoRegimenSalud pslist1 : pslist) {
            if (pslist1.isVigente()) {
                bandera2 = true;
            }
        }

        List<PeriodoTipoTrabajador> ptlist = testeo.getPeriodoTipoTrabajadorList();
        Boolean bandera3 = false;
        for (PeriodoTipoTrabajador ptlist1 : ptlist) {
            if (ptlist1.isVigente()) {
                bandera3 = true;
            }
        }

        Boolean bandera4 = false;
        if (testeo.getVigente()) {
            bandera4 = true;
        }

        if (bandera && bandera2 && bandera3 && bandera4) {
            return true;
        }
        return false;
    }

